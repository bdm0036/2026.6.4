    private void seedBooks() {
        Integer count = jdbc.queryForObject(
            "SELECT COUNT(*) FROM bookstore_product.tb_book", Integer.class);
        if (count != null && count >= 50) return;

        int seeded = 0;
        for (String[] b : SEED_BOOKS) {
            try {
                int catIdx = Integer.parseInt(b[5]) - 1;
                String catName = catIdx >= 0 && catIdx < SEED_CATEGORIES.length
                    ? SEED_CATEGORIES[catIdx][0] : b[5];
                Long categoryId = jdbc.queryForObject(
                    "SELECT id FROM bookstore_product.tb_category WHERE name = ? LIMIT 1",
                    Long.class, catName);
                if (categoryId == null) continue;

                String title = b[0], author = b[1], isbn = b[2], publisher = b[3];
                BigDecimal price = new BigDecimal(b[4]);
                String tags = b.length > 6 ? b[6] : "";
                int stock = 5 + new Random().nextInt(96); // 5-100

                jdbc.update(
                    "INSERT IGNORE INTO bookstore_product.tb_book " +
                    "(title, author, isbn, publisher, price, stock, description, category_id, tags, status, is_seed, create_time) " +
                    "VALUES (?, ?, ?, ?, ?, ?, CONCAT('《', ?, '》——', COALESCE(?, ''), '一本值得阅读的好书。'), ?, ?, 1, 1, NOW() - INTERVAL ? DAY)",
                    title, author, isbn, publisher, price, stock, title,
                    tags.length() > 0 ? tags + "，" : "", categoryId, tags,
                    10 + new Random().nextInt(365));
                seeded++;
            } catch (Exception e) {
                log.warn("Seed book failed: {} - {}", b[0], e.getMessage());
            }
        }
        log.info("Seeded {} books across {} categories", seeded, SEED_CATEGORIES.length);
    }

